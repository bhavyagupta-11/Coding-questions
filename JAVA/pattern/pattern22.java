package JAVA.pattern;

public class pattern22 {
    public static void main(String[] args) 
    {
            int x,i;

        for (x=1;x<=4;x++) 
        {
            for (i=x;i<=4;i++) 
            {
                System.out.print(" ");
            }
            for(i=1;i<=x;i++) 
            {
                System.out.print("*");
            }
            System.out.println();
            }
        
    }
}
